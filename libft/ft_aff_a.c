#include "libft.h"

void	ft_aff_a(char *str)
{
	if (str == NULL)
		return ;
	while (*str)
	{
		if (*str == 'a')
		{
			ft_putchar(*str);
			break ;
		}
		str++;
	}
	ft_putchar('\n');
}
