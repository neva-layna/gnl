#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	sym;
	size_t			i;

	sym = c;
	i = 0;
	while (i < len)
	{
		((char *)b)[i] = sym;
		i++;
	}
	return (b);
}
